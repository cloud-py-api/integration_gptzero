					:files="selectedFiles"
					@remove-file-selection="removeFile" />
				<div class="actions">
					<NcButton :disabled="predicting"
						type="primary"
						@click="getResults">
						<template #icon>
							<span v-if="predicting" class="material-icon icon-loading-small" />
							<GPTZeroIcon v-else :size="18" />
						</template>
